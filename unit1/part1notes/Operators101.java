package ghar.javawork.virtual.unit1.part1notes;

public class Operators101 {	public static void main(String[] args)
{
    int num1; //Declaring variable
    num1 = 5; // = is called the 'assignment operator'. Initializing the variable.

    System.out.println(num1);

    int num2 = 12; // It is possible (and preferable) to declare and initialize variables in the same command statement.

    System.out.println(num2);

    num1 = 8; // only declare the variable once, can change the value many times.

    System.out.println(num1);

    System.out.println(num2); // Printing the contents of the variable
    System.out.println("num2"); // Printing a String Literal

    int num3 = 15;
    System.out.println(num3);

    num3 = 3;

    System.out.println(num3);

}
}
