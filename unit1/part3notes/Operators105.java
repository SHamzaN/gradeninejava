package ghar.javawork.virtual.unit1.part3notes;

// This program demonstrates the five real number <double> operations.

public class Operators105
{
    public static void main(String[] args)
    {
        double a = 0;
        double b = 10.0;
        double c = 3.333333333;

        a = b + c; // Addition
        System.out.println(b + " + " + c + " = " + a);

        a = b - c; // Subtraction
        System.out.println(b + " - " + c + " = " + a);

        a = b * c; // Multiplication
        System.out.println(b + " * " + c + " = " + a);

        a = b / c; // Division
        System.out.println(b + " / " + c + " = " + a);

        a = b % c; // Modulus Division
        System.out.println(b + " % " + c + " = " + a);


    }
}