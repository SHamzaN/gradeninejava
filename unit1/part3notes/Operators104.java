package ghar.javawork.virtual.unit1.part3notes;

// This program demonstrates the five integer operations.

public class Operators104
{
    public static void main(String[] args)
    {
        int a = 0;
        int b = 25;
        int c = 10;

        a = b + c; // Addition
        System.out.println(b + " + " + c + " = " + a);

        a = b - c; // Subtraction
        System.out.println(b + " - " + c + " = " + a);

        a = b * c; // Multiplication
        System.out.println(b + " * " + c + " = " + a);

        a = b / c; // Division
        System.out.println(b + " / " + c + " = " + a);

        a = b % c; // Modulus Division
        System.out.println(b + " % " + c + " = " + a);


    }
}